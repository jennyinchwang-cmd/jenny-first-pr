def digital_root(n: int) -> int:
    while n > 9:
        n = sum(int(c) for c in str(n))
    return n if n > 0 else 9


def sum_analysis(full_number: str) -> dict:
    """วิเคราะห์ผลรวมของเบอร์เต็ม (รวมรหัส 09)"""
    digits = [int(c) for c in full_number if c.isdigit()]
    total = sum(digits)
    root = digital_root(total)
    star, star_txt = _ROOT_STAR[root]
    if total in GOOD_SUMS:
        tone, text, bonus = "ดี", GOOD_SUMS[total], +1.5
    elif total in CAUTION_SUMS:
        tone, text, bonus = "ระวัง", CAUTION_SUMS[total], -1.5
    else:
        tone, text, bonus = "กลาง", f"ผลรวมระดับกลาง ไม่ฉุดไม่หนุนชัดเจน พลังหลักไหลไปตามดาวประจำผลรวมคือ{star}", 0.0
    return {"total": total, "tone": tone, "text": text, "bonus": bonus,
            "root": root, "root_star": star, "root_text": star_txt}


def myanmar_analysis(full_number: str) -> dict:
    """มิติเลขศาสตร์พม่า — นวิน (เลข 9), นัต 37, เลขเรียงมงคล"""
    digits = [c for c in full_number if c.isdigit()]
    s = "".join(digits)
    nines = s.count("9")
    total = sum(int(c) for c in digits)
    notes, bonus = [], 0.0

    if nines >= 4:
        notes.append(f"มีเลข 9 ถึง {nines} ตัว — 'โกนะวิน' กำลังแรงมาก ตามคติพม่าเลข 9 คือพุทธคุณ 9 ประการ ยิ่งมีมากยิ่งหนุนความสำเร็จและความคุ้มครอง")
        bonus += 1.0
    elif nines >= 2:
        notes.append(f"มีเลข 9 จำนวน {nines} ตัว — พลังนวิน (พุทธคุณ 9 ประการ) หนุนนำความเจริญตามคติพม่า")
        bonus += 0.5
    if "999" in s:
        notes.append("มีชุด 999 — สุดยอดเลขมงคลของพม่า เป็นที่ต้องการสูง เสริมทั้งดวงและมูลค่าเบอร์")
        bonus += 0.5
    if total % 9 == 0:
        notes.append(f"ผลรวม {total} หารด้วย 9 ลงตัว — ครบกำลังนวิน ถือเป็นเบอร์ที่พลัง 9 คุมทั้งเบอร์")
        bonus += 0.5
    if "37" in s:
        notes.append("มีเลข 37 — ตรงกับจำนวนนัตทั้ง 37 ตนที่ชาวพม่านับถือ เชื่อว่ามีสิ่งศักดิ์สิทธิ์พื้นเมืองคุ้มครอง")
    if not notes:
        notes.append("ไม่มีสัญลักษณ์นวินเด่นชัด พลังของเบอร์มาจากโครงสร้างคู่เลขเป็นหลัก")
    return {"nines": nines, "notes": notes, "bonus": min(bonus, 2.0)}
