_CONNECT = ["อีกทั้ง", "นอกจากนี้", "ขณะเดียวกัน", "และ"]

def pair_paragraph(pair: str, power: int) -> str:
    """สร้างย่อหน้าคำทำนายยาวของคู่เลขหนึ่งคู่ — โทนตามพลัง (บวก=ส่งเสริม, ลบ=เตือน)"""
    a, b = pair[0], pair[1]
    da, db = DIGIT[a], DIGIT[b]
    La, Lb = DIGIT_LONG[a], DIGIT_LONG[b]
    m = pair_meaning(pair)
    same = (a == b)

    parts = []
    # เปิดด้วยหัวใจของคู่
    parts.append(f"อิทธิพลของคู่เลข {pair} ({m['title']}) — {m['desc']}")

    def fuse(key, label):
        if same:
            return f"{label} {La[key]}"
        return f"{label} {La[key]} {_CONNECT[hash(pair+key) % len(_CONNECT)]}{Lb[key]}"

    if power >= 2:      # คู่ดี — เล่าเชิงส่งเสริมครบทุกด้าน
        parts.append(fuse("money", "ด้านการเงิน"))
