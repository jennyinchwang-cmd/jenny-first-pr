def pair_meaning(pair: str) -> dict:
    """คืนความหมายของคู่เลข (dict: title, desc, money, work, love, health, char)"""
    a, b = pair[0], pair[1]
    da, db = DIGIT[a], DIGIT[b]
    title = f"{da['name']}-{db['name']}"
    if pair in SPECIAL:
        desc = SPECIAL[pair]
    elif a == b:
        desc = f"พลัง{da['name']}ซ้อนสองชั้น — {da['trait']} เข้มข้นเป็นพิเศษ"
    else:
        desc = f"{da['trait']} ผสาน {db['trait']}"
    out = {"pair": pair, "title": title, "desc": desc}
    for k in _ASPECT_KEYS:
        out[k] = f"{da[k]} ประกอบกับ{db[k]}" if a != b else da[k]
    return out


